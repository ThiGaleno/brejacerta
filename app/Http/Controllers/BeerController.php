     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        try {
            $allBeers = Beer::all();
            $beers = $allBeers->toArray();
            foreach($allBeers as $key => $value){
                if($value->rating){
                    $beers[$key]["rating"] = $this->getMediaRatings($value->rating);
                }
            }
            return response($beers, 200);
        } catch (\Throwable $th) {
            return response($th, 500);
        }

    }

    private function getMediaRatings($ratings){
        $ratingAmount = count($ratings);
        $rate = 0;
        foreach($ratings as $value){
            $rate = $rate + $value->rated;
        }
        return number_format($rate / $ratingAmount, 2);
    }
